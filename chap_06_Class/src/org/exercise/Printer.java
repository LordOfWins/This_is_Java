package org.exercise;

public class Printer {

    private Printer() {
    }

    static void println(int parameter) {
        System.out.println(parameter);
    }

    static void println(boolean parameter) {
        System.out.println(parameter);
    }

    static void println(double parameter) {
        System.out.println(parameter);
    }

    static void println(String parameter) {
        System.out.println(parameter);
    }
}
